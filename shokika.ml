(* 12.3 *)
(* shokika: eki_t list -> string -> eki_t list *)
let rec shokika eki_t_list kiten_kanji = match eki_t_list with
  [] -> []
  | ({namae=n; saitan_kyori=s; temae_list=tl} as first) :: rest -> 
    if n = kiten_kanji then {namae=n; saitan_kyori=0.0; temae_list=[n]} :: shokika rest kiten_kanji 
    else first :: shokika rest kiten_kanji

